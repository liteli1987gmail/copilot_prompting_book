# GET /login
# - 显示登录表单。
# - 如果用户已经登录，重定向到他们的个人资料页面。

# POST /login
# - 处理登录表单的提交。
# - 如果登录成功，重定向到用户的个人资料页面并显示成功消息。
# - 如果登录失败，重新显示登录表单并显示错误消息。

# GET /logout
# - 注销当前登录的用户。
# - 重定向到登录页面。

# GET /register
# - 显示注册表单。
# - 如果用户已经登录，重定向到他们的个人资料页面。

# POST /register
# - 处理注册表单的提交。
# - 如果注册成功，重定向到用户的个人资料页面并显示成功消息。
# - 如果注册失败，重新显示注册表单并显示错误消息。

# GET /users/:id
# - 显示具有特定ID的用户的个人资料页面。
# - 期望用户的ID作为URL参数。
# - 如果用户不存在，显示错误消息。